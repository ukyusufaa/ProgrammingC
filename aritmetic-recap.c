#include <stdio.h>

int main(){
    int a=15;
    int b=5;
    int result;

    result=a+b;
    printf("Added result: %d\n",result);

    result=a-b;
    printf("Subtracted result: %d\n",result);

    result=a*b;
    printf("multiplied result: %d\n",result);

    result=a/b;
    printf("Divided result: %d\n",result);


    return 0;

}
